package main

import "fmt"

func main() {
	var name string

	//contoh variable
	name = "Muhammad Aulia Rahman"
	fmt.Println(name)

	//contoh variable
	name = "Aulia Rahman"
	fmt.Println(name)

	//contoh variable
	var mobil = "Lamborgini"
	fmt.Println(mobil)

	//contoh variable
	var namatempat = "Cikole"
	fmt.Println(namatempat)

	// cetak var
	var merkhp = "iphone"
	fmt.Println(merkhp)

	// variable int
	var umur = 30
	fmt.Println(umur)

	// Mempersingkat variable
	religion := "islam"
	fmt.Println(religion)

	// Multiple Variable
	var (
		firstname = "Muhammad Aulia"
		lastname  = "Rahman"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}
